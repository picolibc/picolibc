        .syntax unified
        .section .text.syscall
        .align 4
        .p2align 4,,15
        .globl syscall
        .type syscall,%function
#ifdef __thumb__
	.thumb
#endif
syscall:
	mov	ip, sp
	push	{r4, r5, r6, r7}
	mov	r7, r0
	mov	r0, r1
	mov	r1, r2
	mov	r2, r3
	ldmfd	ip, {r3, r4, r5, r6}
	swi	0x0
	pop	{r4, r5, r6, r7}
	cmn	r0, #4096
	it	cc
        bxcc    lr
	b	_syscall_error

        .size syscall, . - syscall
