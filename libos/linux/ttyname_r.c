#include "local-linux.h"
#include <limits.h>
#include <unistd.h>
#include <stdio.h>

int
ttyname_r(int fd, char *buf, size_t bufsize)
{
    char    fdpath[sizeof("/proc/self/fd/") + 12];
    int     iret;
    ssize_t ssret;

    iret = snprintf(fdpath, sizeof(fdpath), "/proc/self/fd/%d", fd);
    if (iret < 0)
        return iret;
    ssret = readlink(fdpath, buf, bufsize);
    if (ssret < 0)
        return -1;
    return 0;
}
